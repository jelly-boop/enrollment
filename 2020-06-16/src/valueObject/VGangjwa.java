package valueObject;

public class VGangjwa {
	private String id;
	private String gangjwaName;
	private String gyosuName;
	private String hakjeom;
	private String time;
	
	public VGangjwa() {
	}

	public String getId() {return id;}
    public void setId(String id) {this.id = id;}

	public String getGangjwaName() {return gangjwaName;}
	public void setGangjwaName(String gangjwaName) {this.gangjwaName = gangjwaName;}

	public String getGyosuName() {return gyosuName;}
	public void setGyosuName(String gyosuName) {this.gyosuName = gyosuName;}

	public String getHakjeom() {return hakjeom;}
	public void setHakjeom(String string) {this.hakjeom = string;}

	public String getTime() {return time;}
	public void setTime(String time) {this.time = time;}



}
